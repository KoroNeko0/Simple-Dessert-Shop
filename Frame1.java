    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
       
        //Create Store Button:
        
        String shopName ,managerName , ageStr , sizeStr ;
        shopName = jTextField1.getText();
        managerName = jTextField2.getText();
        ageStr = jTextField3.getText();
        sizeStr = jTextField4.getText();
        int age ;
        int size ;
        
        if(  shopName.equals("") ||  managerName.equals("") || ageStr.equals("") || sizeStr.equals("") ){
            JOptionPane.showMessageDialog(this, "Please Complete the Informations !" , "WARNING" , JOptionPane.WARNING_MESSAGE);
            return;
        }
        
        try{
            
        age = Integer.parseInt(ageStr);
         
        size = Integer.parseInt(sizeStr);
        
        if(age < 0 || size < 0)
            throw new NegativeNumberException();
        
        }catch(NumberFormatException e){
                JOptionPane.showMessageDialog(this, "Please Enter an Integer Number !" , "WARNING" , JOptionPane.WARNING_MESSAGE);
                jTextField3.setText("");
                jTextField4.setText("");
                return;
         }catch(NegativeNumberException e){
             JOptionPane.showMessageDialog(this, "Please Enter a Positive Number !", "WARNING" , JOptionPane.WARNING_MESSAGE);
             jTextField3.setText("");
             jTextField4.setText("");
            return;
         }
        
        shop = new DessertShop(shopName , size , managerName , age);
        shop.SaveFile();
        JOptionPane.showMessageDialog(this, shopName+" Store is Created !", "Creation Alert" , JOptionPane.INFORMATION_MESSAGE);
        jButton1.setEnabled(false);
        jButton2.setEnabled(true);
        jButton3.setEnabled(true);
        jTextField1.setEditable(false);
        jTextField2.setEditable(false);
        jTextField3.setEditable(false);
        jTextField4.setEditable(false);

    }//GEN-LAST:event_jButton1ActionPerformed

